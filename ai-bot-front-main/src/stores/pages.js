import { defineStore } from 'pinia';
import axios from 'axios';
import { apiCache, createCacheKey } from '@/utils/cacheUtils';

export const usePageStore = defineStore('pages', {
    state: () => ({
        pages: {},
        page: null,
        isLoaded: false,
        currentLocale: 'ru'
    }),
    actions: {
        async fetchData(locale = 'ru') {
            // ???????? ???????????? ???? ???????????????????? ?? ???????????? ?????? ??????????????????, ???? ??????????????????????????
            if (this.isLoaded && this.currentLocale === locale) return;

            const cacheKey = createCacheKey('/pages', { lang: locale });
            const cached = apiCache.get(cacheKey);
            if (cached) {
                this.pages = cached;
                this.currentLocale = locale;
                this.isLoaded = true;
                return;
            }

            try {
                const response = await axios.get('/pages', {
                    params: { lang: locale }
                });
                // ????????????????????, ?????? response.data - ?????? ????????????
                if (response.data && typeof response.data === 'object') {
                    this.pages = response.data;
                    this.currentLocale = locale;
                    this.isLoaded = true;
                    // ???????????????? ???? 10 ??????????
                    apiCache.set(cacheKey, response.data, 10 * 60 * 1000);
                } else {
                    console.warn('Invalid pages data received:', response.data);
                    this.pages = {};
                }
            } catch (error) {
                console.error('Error fetching pages:', error);
                // ?? ???????????? ???????????? ?????????????????????????? ???????????? ????????????, ?????????? ???? ?????????????????????? ??????????????????
                this.pages = {};
                this.isLoaded = false;
            }
        },
        setPage(payload) {
            this.page = payload;
        }
    }
});
